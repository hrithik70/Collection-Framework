package Iterable.Collections.List.LinkedList;

import java.util.LinkedList;

public class LinkedListCreate {

	public static void main(String[] args) {

		
		// Linked List of int Data Type
		LinkedList<Integer> list1 = new LinkedList<Integer>();
		list1.add(1);
		list1.add(2);
		list1.add(3);
		list1.add(4);
		list1.add(5);
		System.out.println("Linked List (int type) :- " + list1);
		
		
		// Linked List of String Data Type
		LinkedList<String> list2 = new LinkedList<String>();
		list2.add("Ram");
		list2.add("Raju");
		list2.add("Shyam");
		list2.add("Rahul");
		list2.add("Ramesh");
		System.out.println("Linked List (String type) :- " + list2);
		
		
		
		
		

	}

}
